public class TDInfo {
    String id;
    String lx1;
    String bt;
    String lx2;
    String addr;
    String nr;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getLx1() {
        return lx1;
    }

    public void setLx1(String lx1) {
        this.lx1 = lx1;
    }

    public String getBt() {
        return bt;
    }

    public void setBt(String bt) {
        this.bt = bt;
    }

    public String getLx2() {
        return lx2;
    }

    public void setLx2(String lx2) {
        this.lx2 = lx2;
    }

    public String getAddr() {
        return addr;
    }

    public void setAddr(String addr) {
        this.addr = addr;
    }

    public String getNr() {
        return nr;
    }

    public void setNr(String nr) {
        this.nr = nr;
    }
}
